# A for loop is used to iterate through a sequence like list, tuple, or string [iterables] 

'''
Syntax: 
l = [1, 7, 8] 
for item in l: 
    print(item) # prints 1, 7 and 8
    
'''

print("--------------list----------------")
li = ["ankit",True, 3, 4.2, "mohan", "vidya"]

for i in li :
    print(i)
    
    
print("--------------tuple----------------")
tup = (4,5,2,4535,334234)
for i in tup:
  print(i)

    
print("---------------string---------------")
str = "ankit" 
for i in str:
    print(i)
     